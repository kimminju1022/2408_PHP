// 전역  스크립트 내 어디에서도 붙어 사용 가능함 $str;쓰면 에러표시가 뜸
// 이유 : 함수영역에서는 단순히 변수명만 적어줘서는 안된다 그렇기 때문에 global $str;을 선언해주어 전역스코프를 사용할 수 있도록 한다

$str = "전역스코프";
function my_scope1(){
    global $str;
    echo $str;
}
echo"\n";

// 지역 스크립트 내에서 사용 가능함 밖에서 쓰면 에러가 뜸
