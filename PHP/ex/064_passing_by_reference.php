<?php

$num1 = 100;
$num2 = &$num1;  //&을 붙이면 $num1에도 영향을 준다

$num2 -= 50;
// copy of value 방식 : 값을 복사한다 즉 본 예제의 $num2는 $num1에서 100을 가져와 저장하고 -=으로 대입연산자기 때문에 50이 차감됨
echo $num1, $num2;

// 참조형
$num3 = 100;
$num4 = &$num3;

$num4 -= 50;
echo $num3,$num4; //num3=100 num4=50 

echo "\n";

